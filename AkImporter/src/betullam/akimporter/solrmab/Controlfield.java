package betullam.akimporter.solrmab;

public class Controlfield {

	private String tag;
	private String controlfieldText;

	public Controlfield() {}


	public String getTag() {
		return this.tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}
	
	public String getControlfieldText() {
		return this.controlfieldText;
	}

	public void setControlfieldText(String controlfieldText) {
		this.controlfieldText = controlfieldText;
	}
	
}
