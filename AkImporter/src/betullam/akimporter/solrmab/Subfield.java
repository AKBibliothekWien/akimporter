package betullam.akimporter.solrmab;

public class Subfield {

	private String code;
	private String subfieldText;
	
	public Subfield() {}

	public String getCode() {
		return this.code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getSubfieldText() {
		return this.subfieldText;
	}

	public void setSubfieldText(String subfieldText) {
		this.subfieldText = subfieldText;
	}
	
}
